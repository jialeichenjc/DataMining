package com.company;

public class Point2D {
    public int id;
    public float x;
    public float y;

    public Point2D(int id, float x, float y) {
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public Point2D() {
        this.id = 0;
        this.x = 0.0f;
        this.y = 0.0f;
    }

}
