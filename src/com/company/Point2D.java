package com.company;

public class Point2D {
    public int id;
    public double x;
    public double y;

    public Point2D(int id, double x, double y) {
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public Point2D() {
        this.id = 0;
        this.x = 0.0;
        this.y = 0.0;
    }

}
